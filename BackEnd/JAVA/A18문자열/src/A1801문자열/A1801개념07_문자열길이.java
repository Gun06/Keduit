package A1801문자열;



public class A1801개념07_문자열길이 {
	public static void main(String[] args) {
		
		
		int[] arr = {10, 20, 30};
		System.out.println("배열의 길이 = " + arr.length); 
		
		
		String str = "itacademy";
		// [주의] 문자열은 () 가 붙는다. 
		System.out.println("문자열의 길이 = " + str.length());
		
		int size = str.length();
		System.out.println(size);
		
	
		
	}
}
