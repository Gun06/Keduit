package A1801문자열;
/*
	문자 한글자는 character 라고하며 길이가 길기때문에 char 를 사용한다. 
	char 는 한글자만 저장가능하며 홑따옴표 '' 를 감싼다. 
	  	
*/

public class A1801개념01_char {
	public static void main(String[] args) {
		
		char a = 'a';
		System.out.println(a);
		
		char b = '김';
		System.out.println(b);
		
		// 한글자만 저장가능하다. 아래주석을 풀면 에러가 발생한다.
		//char c = 'ab';
		
	}
}
