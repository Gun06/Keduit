package A0102출력문;
/*
	[자료형문자열]
	[1] 여러글자를 문자열이라고하며 String으로 사용한다.
	[2] 첫글자가 대문자이다. 
	[3] "" 쌍따옴표(큰따옴표)로 감싸서 표현한다.
	[4] 한글자라도 쌍따옴표로 감싸면 문자열이다.
*/
public class A0102개념06_자료형_문자열 {
	public static void main(String[] args) {
		
		System.out.println("[여러 글자");
		System.out.println("Java Class");
		System.out.println("안녕하세요 반가워요");
		System.out.println("김"); // 한글자 라도 쌍따옴표로 감싸면 문자열이다.
	}
}
