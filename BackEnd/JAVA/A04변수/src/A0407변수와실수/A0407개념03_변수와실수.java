package A0407변수와실수;
/*
'''
[변수와 실수]
지금까지는 변수에 정수형 데이터만 저장하여 사용했습니다.
이제부터는 변수에 실수형 데이터를 저장하고, 이를 활용한 산술 연산을 수행해 보겠습니다.
'''
 */
public class A0407개념03_변수와실수 {
	public static void main(String[] args) {
		
		double a = 140.5;
		double b = 78.3;
		
		double c = a + b;
		System.out.println(c);
		
		c = a - b;
		System.out.println(c);
		
		c = a * b;
		System.out.println(c);
		
		c = a / b;
		System.out.println(c);
	}
}
