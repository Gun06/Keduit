package A0401변수;

/*
	# 변수(variable)
		1. 변수란, 단 하나의 값을 저장할 수 있는 메모리 공간
		2. 변수의 선언		: int a;	( 메모리 공간 할당 )
		3. 변수의 초기화 	: a = 10;	( 값 저장)
*/
public class A0401개념01_변수선언과저장 {
	public static void main(String[] args) {

		
		int a;
		a = 10;
		System.out.println("[정수]");
		System.out.println(a);
		
		int b;
		b = 20;
		System.out.println(b);
		
	}
}
