package A0401변수;
/*
	[대입 연산자]
	변수의 값을 저장할 때 사용하는 = 연산자를 대입 연산자라고 부릅니다.
	
	[대입 연산자의 역할]
	프로그래밍에서 = 연산자는 우리가 알고 있는 산수에서의 =와는 뜻이 다릅니다.
	대입 연산자는 오른쪽의 값을 왼쪽의 변수에 대입(복사)하여 저장한다는 의미를 갖고 있습니다.
 */
public class A0401개념04_대입연산자 {
	public static void main(String[] args) {
		
		int x = 8;
		System.out.println(x);
	}
}
