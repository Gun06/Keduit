package A0204양수와음수;
/*
	[곱하기의 교환법칙]
	곱하기의 경우, 숫자의 순서를 바꾸어도 결과가 같습니다.
	예를 들어, 3 * 5와 5 * 3는 둘 다 15이므로, 곱하기의 교환법칙이 성립합니다.
 */
public class A0204개념05_곱하기의교환법칙 {
	public static void main(String[] args) {

		System.out.println(3 * 5);       
		System.out.println(5 * 3); 
		
		System.out.println(3 * -5);       
		System.out.println(-5 * 3); 
		      
		System.out.println(-3 * -5);      
		System.out.println(-5 * -3);     
	}
}
