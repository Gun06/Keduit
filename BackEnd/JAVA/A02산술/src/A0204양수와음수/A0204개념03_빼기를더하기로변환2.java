package A0204양수와음수;
/*
	[빼기를 더하기로 변환2]
	양수와 음수의 빼기는 더하기로 변환할 수 있습니다.
	예를 들어, 7 - -4에서 빼기 기호(-)가 두 번 연달아 있으면, 이는 부호가 바뀌어 더하기로 변환됩니다.
	그 결과 7 + 4로 변환할 수 있습니다.
 */
public class A0204개념03_빼기를더하기로변환2 {
	public static void main(String[] args) {
		System.out.println(7 - -4);
		System.out.println(7 + 4);
	}
}
