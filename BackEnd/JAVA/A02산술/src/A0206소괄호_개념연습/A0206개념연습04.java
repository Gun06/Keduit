package A0206소괄호_개념연습;
/*
	[문제]
	다음 보기는 혼합 계산식입니다.
	주어진 데이터를 사용하여 연산의 결과를 출력하는 코드를 작성하고, 각 연산의 결과를 예측하시오.
	
	[보기]
	6 - (-5 + 4) - 7
	9 - 3 - (7 + -2)
	8 - (-2 + 5) + 1
	8 + -2 - (8 + 3)
 */
