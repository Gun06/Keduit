package A0205혼합계산;
/*
	[혼합 계산]
	연산자가 두 개 이상 포함된 식을 혼합 계산식이라고 부릅니다.
	혼합 계산식에서는 연산자의 우선순위를 고려하여 계산됩니다.
 */
public class A0205개념03_혼합계산 {
	public static void main(String[] args) {
		System.out.println(6 + 5 * 3);        
		System.out.println(-3 - 8 * 6);           
		System.out.println(5 + 3 / 4);           
		System.out.println(-8 - 3 / 3);          
		System.out.println(2 + 5 % 5);            
		System.out.println(-9 - 7 % 2);           
	}
}
