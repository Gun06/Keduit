package A0207실수;
/*
	[연결연산자]
		문자와 문자를 연결하는것을 연결연산자를 사용해서 할수있다. 
		예를들어 "안녕" + "하세요" 는 "안녕하세요" 가된다.
		
		여기서 + 는 일반적인 더하기가 아니라 연결연산자로 적용된다. 
		연결연산자는 여러개도 연결할수있다. 
		연결연산자는 오로지 + 만 적용된다.
*/
public class A0207개념07_연결연산자 {
	public static void main(String[] args) {
		System.out.println("안녕" + "하세요");
		System.out.println("반" + "갑" + "습" + "니다");
	}

}
