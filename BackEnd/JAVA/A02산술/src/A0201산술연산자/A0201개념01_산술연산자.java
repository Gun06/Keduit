package A0201산술연산자;
/*
[산술 연산자]
	1. 더하기			+
	2. 빼기			-
	3. 곱하기			*
	4. 나누기			/
	5. 나머지			%

*/
public class A0201개념01_산술연산자 {
	public static void main(String[] args) {
					
		System.out.println(10 + 3);
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		
		// 나누기와 나머지는 [나누기와몫] 과 [나머지] 단원에서 다룬다. 
	}
}
