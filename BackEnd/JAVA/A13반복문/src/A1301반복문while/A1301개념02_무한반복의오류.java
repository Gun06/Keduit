package A1301반복문while;

/*
	[무한반복의 오류]
	반복문을 설계할 때, 논리적 오류가 있으면 반복문이 멈추지 않고 계속 실행되는
	무한 반복 현상이 발생할 수 있습니다.
	이를 방지하기 위해서는 조건식을 논리적으로 올바르게 작성해야 합니다.
	
	만약 무한 반복이 발생한 경우, 터미널 창에서 휴지통 모양 아이콘을 눌러
	프로그램을 강제로 종료한 후, 코드를 수정하여 다시 실행해야 합니다.
 */

public class A1301개념02_무한반복의오류 {
	public static void main(String[] args) {
		
		int i = 0;
		
		while(i < 5) {
			// i를 계속해서 1씩 감소하여 i < 5 조건을 항상 만족하므로 무한 반복됩니다.
			System.out.println(i);
			i -= 1;
		}
		
	}
}
