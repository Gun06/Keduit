package A0602비교연산자;

/*
	[boolean 자료형]
	boolean 자료형은 참(true) 또는 거짓(false)의 두 가지 값만을 저장하는 논리 자료형입니다.
	이 자료형의 이름은 영국 수학자 조지 불(George booleane)의 이름에서 유래되었습니다.
	
	[변수와 boolean 자료형]
	참(true)과 거짓(false)의 boolean 값은 변수에 저장하여 사용할 수 있습니다.
*/

public class A0602개념02_참과거짓 {
	public static void main(String[] args) {
		
		boolean a = true;
		System.out.println(a);
		
		boolean b = false;
		System.out.println(b);
		
		boolean c = 10 == 10;
		System.out.println(c);
		
		boolean d = 10 != 10;
		System.out.println(d);
		
	}
}
