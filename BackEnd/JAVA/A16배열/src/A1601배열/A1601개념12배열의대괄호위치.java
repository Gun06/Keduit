package A1601배열;


/*
	[배열선언시 대괄호위치]
		배열선언시 [] 의 위치는 변수앞이나 뒤에 위치한다. 
		하지만 변수앞에 위치하는것이 일반적이다. 
	
*/
public class A1601개념12배열의대괄호위치 {
	public static void main(String[] args) {
		int [] arr1 = null;
		arr1 = new int[4];
		
		int arr2[] = null;
		arr2 = new int[4];
		
		int [] arr3 = new int[4];
		int arr4[] = new int[4];
		
		int [] arr5 = {10, 20, 30, 40};
		int arr6[] = {10, 20, 30, 40};
		
	}
}
