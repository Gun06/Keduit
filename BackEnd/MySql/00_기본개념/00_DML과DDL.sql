/* 
	[데이터 조작 언어, DML(Data Manipulation Language)]
    : 테이블에서 데이터를 조회, 삽입, 수정, 삭제하는 명령어
    -----------------------------------------------------------
    - SELECT(조회)
    - INSERT(삽입)
    - UPDATE(수정)
    - DELETE(삭제)
 */
 
 /*
	[데이터 정의 언어, DDL(Data Definition Language)]
    : 데이터베이스, 테이블, 뷰, 인덱스 등의 개체를 생성, 삭제, 변경하는 명령어
    -----------------------------------------------------------
    - CREATE(생성)
    - DROP(삭제)
    - ALTER(변경)
 */